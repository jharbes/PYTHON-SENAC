# Calcula a idade em dias

anos=input("Informe o numero de anos de idade que a pessoa possui: ")
anos=int(anos)
meses=input("Informe o numero de meses de idade que a pessoa possui: ")
meses=int(meses)
dias=input("Informe o numero de dias de idade que a pessoa possui: ")
dias=int(dias)

print("\n")
print("O total de dias de idade dessa pessoa eh",(anos*365)+(meses*30)+dias,"dias.")
